ws = wb.active
ws.title = '후기태그 전체'

header_font = Font(name='맑은 고딕', size=11, bold=True, color='FFFFFF')
header_fill = PatternFill('solid', fgColor='1A1A2E')
mapped_fill = PatternFill('solid', fgColor='FFF8E7')   # 노란빛 — 이미 매핑된 행
unmapped_fill = PatternFill('solid', fgColor='FFFFFF')
unclassified_fill = PatternFill('solid', fgColor='F0EDE6')
border = Border(
    left=Side(style='thin', color='D0CEC7'),
    right=Side(style='thin', color='D0CEC7'),
    top=Side(style='thin', color='D0CEC7'),
    bottom=Side(style='thin', color='D0CEC7'),
)
center = Alignment(horizontal='center', vertical='center')
left_align = Alignment(horizontal='left', vertical='center')

headers = ['그룹', '매핑후기 태그', '빈도', 'ID', 'tagID', '제안그룹(Q#-#)']
for col, h in enumerate(headers, 1):
    c = ws.cell(1, col, h)
    c.font = header_font
    c.fill = header_fill
    c.alignment = center
    c.border = border

for i, (grp, tag, cnt, idval, tagid, suggested) in enumerate(rows, start=2):
    fill = mapped_fill if idval else (unclassified_fill if grp == 'Z. 미분류' else unmapped_fill)
    ws.cell(i, 1, grp).alignment = left_align
    ws.cell(i, 2, tag).alignment = left_align
    ws.cell(i, 3, cnt).alignment = center
    ws.cell(i, 4, idval).alignment = center
    ws.cell(i, 5, tagid).alignment = left_align
    ws.cell(i, 6, suggested).alignment = center
    for col in range(1, 7):
        ws.cell(i, col).fill = fill
        ws.cell(i, col).border = border

ws.column_dimensions['A'].width = 30
ws.column_dimensions['B'].width = 22
ws.column_dimensions['C'].width = 8
ws.column_dimensions['D'].width = 12
ws.column_dimensions['E'].width = 18
