# ─────── 통합 행 데이터 만들기 ───────
mapped_set = set()
rows = []  # (그룹, 매핑후기태그, 빈도, ID, tagID, 제안그룹)

for grp, tag, idval, tagid in MAPPED:
    cnt = all_tags.get(tag, 0)
    rows.append((grp, tag, cnt, idval, tagid, idval))  # 이미 매핑됨 → 제안=자기자신
    mapped_set.add(tag)

for grp, items in UNMAPPED_GROUPS.items():
    for tag, suggested in items:
        if tag in mapped_set:
            continue  # 매핑 영역에 이미 있음, 중복 방지
        cnt = all_tags.get(tag, 0)
        if cnt == 0:
            continue  # DB에 없는 태그 (오타 등)
        rows.append((grp, tag, cnt, '', '', suggested))
        mapped_set.add(tag)

# 위 그룹 어디에도 안 들어간 나머지 (미분류) → 별도 행
for tag, cnt in all_tags.items():
    if tag in mapped_set:
        continue
    rows.append(('Z. 미분류', tag, cnt, '', '', ''))

# 정렬: 그룹 알파벳/한글 → 빈도 내림차순
rows.sort(key=lambda r: (r[0], -r[2]))

# ─────── 엑셀 생성 ───────
